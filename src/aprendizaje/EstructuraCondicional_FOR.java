package aprendizaje;

public class EstructuraCondicional_FOR {
    public static void main(String[] args) {
        // Estructura de control for
        // Se utiliza para iterar sobre un rango de valores o una colección de
        // elementos.
        // La sintaxis básica es:
        // for (inicialización; condición; incremento) {
        // // Código a ejecutar en cada iteración
        // }

        for (int i = 0; i < 10; i++) {
            System.out.println("El valor de i es: " + i);
        }
        // El bucle for se utiliza comúnmente cuando se conoce el número exacto de
        // iteraciones que se desean realizar.
    }
}
